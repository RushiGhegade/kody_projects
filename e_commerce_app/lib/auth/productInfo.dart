class ProductInfo {
  String? name;
  String? desc;
  String? image;
  int? quantity;
  int? price;
  String? color;
  bool? iscart;

  ProductInfo({
    required this.name,
    required this.desc,
    required this.image,
    required this.price,
    required this.quantity,
    required this.color,
    required this.iscart,
  });
}
